array1 = [1, 2, 3]
array2 = [3, 4, 5]
print "array1 = ", array1, "\n"
print "array2 = ", array2, "\n"

print "array1 + array2 = ", array1 + array2, "\n"

print "array1 - array2 = ", array1 - array2, "\n"

print "array1 * 3 = ", array1 * 3, "\n"

print "array1 * \"-\" = ", array1 * "-", "\n"

print "array1 & array2 = ", array1 & array2, "\n"

print "array1 << 4 = ", array1 << 4, "\n"

print "array1 <=> array2 = ", array1 <=> array2, "\n"

array1[0] = 0
print "do array1[0] = 0, array1 = ", array1, "\n"

print "array1[1.. 2] = ", array1[1, 2], "\n"

array1[1, 2] = 0
print "do array1[1, 2] = 0, array1 = ", array1, "\n"

